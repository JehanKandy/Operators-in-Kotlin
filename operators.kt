fun main(){
    //arithmetic Operators
    //+,-,*,/,%

    val num1 = 12.0
    val num2 = 10.0
    var result:Double

    print("Num1 = $num1 , Num2 = $num2 \n\n")
    //Addition 
    result = num1 + num2
    print("Num1 + Num2 =  $result \n")

    //Substraction
    result = num1 - num2
    print("Num1 - Num2 = $result \n")

    //Multiplication 
    result = num1 * num2
    print("Num1 * Num2 = $result \n")

    //Division
    result = num1 / num2
    print("Num1 / Num2 = $result \n")

    //Modification 
    result = num1 % num2
    print("Num1 % Num2 = $result \n")

}